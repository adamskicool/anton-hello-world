/*
    Variables is something that is not constant (meaning you can change it even after it is set).
    To declare a new variable do the following:
*/
var someones_name = 'Anton Torkkeli'
/*
    What we have now done is created a new variable named someones_name with the value 'Anton Torkkeli'.
    Since it is a variable we can change value of it:
*/
someones_name = 'Adam Johansson'
/*
    The above line (line 10) of code tries to set the value of the variable we created on line 5
    Try and run this file by typing the following in the terminal:
    node part-1/2-what-is-a-variable.js
    What do you think will happen?
*/
console.log(someones_name)